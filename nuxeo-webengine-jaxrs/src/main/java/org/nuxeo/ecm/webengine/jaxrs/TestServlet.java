package org.nuxeo.ecm.webengine.jaxrs;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.nuxeo.ecm.core.api.CoreSession;
import org.nuxeo.ecm.core.api.repository.RepositoryManager;
import org.nuxeo.runtime.api.Framework;

/**
 * @author <a href="mailto:bs@nuxeo.com">Bogdan Stefanescu</a>
 *
 */
public class TestServlet extends HttpServlet {

    private static final long serialVersionUID = 1L;

    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp)
            throws ServletException, IOException {
        StringBuilder buf = new StringBuilder("hello: ");
        try {
            CoreSession cs = Framework.getService(RepositoryManager.class).getDefaultRepository().open();
            buf.append(cs.getRootDocument().getPathAsString()).append(" - ");
            buf.append(cs.getRootDocument().getId());
        } catch (Exception e) {
            e.printStackTrace();
        }
        resp.getWriter().write(buf.toString());
    }

}
